require('proof')(24, function (assert) {
    var tz = require('timezone')(require('timezone/es_PA'))

    // es_PA abbreviated months
    assert(tz('2000-01-01', '%b', 'es_PA'), 'ene', 'Jan')
    assert(tz('2000-02-01', '%b', 'es_PA'), 'feb', 'Feb')
    assert(tz('2000-03-01', '%b', 'es_PA'), 'mar', 'Mar')
    assert(tz('2000-04-01', '%b', 'es_PA'), 'abr', 'Apr')
    assert(tz('2000-05-01', '%b', 'es_PA'), 'may', 'May')
    assert(tz('2000-06-01', '%b', 'es_PA'), 'jun', 'Jun')
    assert(tz('2000-07-01', '%b', 'es_PA'), 'jul', 'Jul')
    assert(tz('2000-08-01', '%b', 'es_PA'), 'ago', 'Aug')
    assert(tz('2000-09-01', '%b', 'es_PA'), 'sep', 'Sep')
    assert(tz('2000-10-01', '%b', 'es_PA'), 'oct', 'Oct')
    assert(tz('2000-11-01', '%b', 'es_PA'), 'nov', 'Nov')
    assert(tz('2000-12-01', '%b', 'es_PA'), 'dic', 'Dec')

    // ' + name + ' months
    assert(tz('2000-01-01', '%B', 'es_PA'), 'enero', 'January')
    assert(tz('2000-02-01', '%B', 'es_PA'), 'febrero', 'February')
    assert(tz('2000-03-01', '%B', 'es_PA'), 'marzo', 'March')
    assert(tz('2000-04-01', '%B', 'es_PA'), 'abril', 'April')
    assert(tz('2000-05-01', '%B', 'es_PA'), 'mayo', 'May')
    assert(tz('2000-06-01', '%B', 'es_PA'), 'junio', 'June')
    assert(tz('2000-07-01', '%B', 'es_PA'), 'julio', 'July')
    assert(tz('2000-08-01', '%B', 'es_PA'), 'agosto', 'August')
    assert(tz('2000-09-01', '%B', 'es_PA'), 'septiembre', 'September')
    assert(tz('2000-10-01', '%B', 'es_PA'), 'octubre', 'October')
    assert(tz('2000-11-01', '%B', 'es_PA'), 'noviembre', 'November')
    assert(tz('2000-12-01', '%B', 'es_PA'), 'diciembre', 'December')
})
