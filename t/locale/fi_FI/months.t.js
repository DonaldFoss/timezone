require('proof')(24, function (assert) {
    var tz = require('timezone')(require('timezone/fi_FI'))

    // fi_FI abbreviated months
    assert(tz('2000-01-01', '%b', 'fi_FI'), 'tammi', 'Jan')
    assert(tz('2000-02-01', '%b', 'fi_FI'), 'helmi', 'Feb')
    assert(tz('2000-03-01', '%b', 'fi_FI'), 'maalis', 'Mar')
    assert(tz('2000-04-01', '%b', 'fi_FI'), 'huhti', 'Apr')
    assert(tz('2000-05-01', '%b', 'fi_FI'), 'touko', 'May')
    assert(tz('2000-06-01', '%b', 'fi_FI'), 'kes채', 'Jun')
    assert(tz('2000-07-01', '%b', 'fi_FI'), 'hein채', 'Jul')
    assert(tz('2000-08-01', '%b', 'fi_FI'), 'elo', 'Aug')
    assert(tz('2000-09-01', '%b', 'fi_FI'), 'syys', 'Sep')
    assert(tz('2000-10-01', '%b', 'fi_FI'), 'loka', 'Oct')
    assert(tz('2000-11-01', '%b', 'fi_FI'), 'marras', 'Nov')
    assert(tz('2000-12-01', '%b', 'fi_FI'), 'joulu', 'Dec')

    // ' + name + ' months
    assert(tz('2000-01-01', '%B', 'fi_FI'), 'tammikuu', 'January')
    assert(tz('2000-02-01', '%B', 'fi_FI'), 'helmikuu', 'February')
    assert(tz('2000-03-01', '%B', 'fi_FI'), 'maaliskuu', 'March')
    assert(tz('2000-04-01', '%B', 'fi_FI'), 'huhtikuu', 'April')
    assert(tz('2000-05-01', '%B', 'fi_FI'), 'toukokuu', 'May')
    assert(tz('2000-06-01', '%B', 'fi_FI'), 'kes채kuu', 'June')
    assert(tz('2000-07-01', '%B', 'fi_FI'), 'hein채kuu', 'July')
    assert(tz('2000-08-01', '%B', 'fi_FI'), 'elokuu', 'August')
    assert(tz('2000-09-01', '%B', 'fi_FI'), 'syyskuu', 'September')
    assert(tz('2000-10-01', '%B', 'fi_FI'), 'lokakuu', 'October')
    assert(tz('2000-11-01', '%B', 'fi_FI'), 'marraskuu', 'November')
    assert(tz('2000-12-01', '%B', 'fi_FI'), 'joulukuu', 'December')
})
