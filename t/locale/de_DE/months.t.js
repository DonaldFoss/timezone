require('proof')(24, function (assert) {
    var tz = require('timezone')(require('timezone/de_DE'))

    // de_DE abbreviated months
    assert(tz('2000-01-01', '%b', 'de_DE'), 'Jan', 'Jan')
    assert(tz('2000-02-01', '%b', 'de_DE'), 'Feb', 'Feb')
    assert(tz('2000-03-01', '%b', 'de_DE'), 'Mär', 'Mar')
    assert(tz('2000-04-01', '%b', 'de_DE'), 'Apr', 'Apr')
    assert(tz('2000-05-01', '%b', 'de_DE'), 'Mai', 'May')
    assert(tz('2000-06-01', '%b', 'de_DE'), 'Jun', 'Jun')
    assert(tz('2000-07-01', '%b', 'de_DE'), 'Jul', 'Jul')
    assert(tz('2000-08-01', '%b', 'de_DE'), 'Aug', 'Aug')
    assert(tz('2000-09-01', '%b', 'de_DE'), 'Sep', 'Sep')
    assert(tz('2000-10-01', '%b', 'de_DE'), 'Okt', 'Oct')
    assert(tz('2000-11-01', '%b', 'de_DE'), 'Nov', 'Nov')
    assert(tz('2000-12-01', '%b', 'de_DE'), 'Dez', 'Dec')

    // ' + name + ' months
    assert(tz('2000-01-01', '%B', 'de_DE'), 'Januar', 'January')
    assert(tz('2000-02-01', '%B', 'de_DE'), 'Februar', 'February')
    assert(tz('2000-03-01', '%B', 'de_DE'), 'März', 'March')
    assert(tz('2000-04-01', '%B', 'de_DE'), 'April', 'April')
    assert(tz('2000-05-01', '%B', 'de_DE'), 'Mai', 'May')
    assert(tz('2000-06-01', '%B', 'de_DE'), 'Juni', 'June')
    assert(tz('2000-07-01', '%B', 'de_DE'), 'Juli', 'July')
    assert(tz('2000-08-01', '%B', 'de_DE'), 'August', 'August')
    assert(tz('2000-09-01', '%B', 'de_DE'), 'September', 'September')
    assert(tz('2000-10-01', '%B', 'de_DE'), 'Oktober', 'October')
    assert(tz('2000-11-01', '%B', 'de_DE'), 'November', 'November')
    assert(tz('2000-12-01', '%B', 'de_DE'), 'Dezember', 'December')
})
