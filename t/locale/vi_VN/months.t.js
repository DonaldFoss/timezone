require('proof')(24, function (assert) {
    var tz = require('timezone')(require('timezone/vi_VN'))

    // vi_VN abbreviated months
    assert(tz('2000-01-01', '%b', 'vi_VN'), 'Th01', 'Jan')
    assert(tz('2000-02-01', '%b', 'vi_VN'), 'Th02', 'Feb')
    assert(tz('2000-03-01', '%b', 'vi_VN'), 'Th03', 'Mar')
    assert(tz('2000-04-01', '%b', 'vi_VN'), 'Th04', 'Apr')
    assert(tz('2000-05-01', '%b', 'vi_VN'), 'Th05', 'May')
    assert(tz('2000-06-01', '%b', 'vi_VN'), 'Th06', 'Jun')
    assert(tz('2000-07-01', '%b', 'vi_VN'), 'Th07', 'Jul')
    assert(tz('2000-08-01', '%b', 'vi_VN'), 'Th08', 'Aug')
    assert(tz('2000-09-01', '%b', 'vi_VN'), 'Th09', 'Sep')
    assert(tz('2000-10-01', '%b', 'vi_VN'), 'Th10', 'Oct')
    assert(tz('2000-11-01', '%b', 'vi_VN'), 'Th11', 'Nov')
    assert(tz('2000-12-01', '%b', 'vi_VN'), 'Th12', 'Dec')

    // ' + name + ' months
    assert(tz('2000-01-01', '%B', 'vi_VN'), 'Tháng một', 'January')
    assert(tz('2000-02-01', '%B', 'vi_VN'), 'Tháng hai', 'February')
    assert(tz('2000-03-01', '%B', 'vi_VN'), 'Tháng ba', 'March')
    assert(tz('2000-04-01', '%B', 'vi_VN'), 'Tháng tư', 'April')
    assert(tz('2000-05-01', '%B', 'vi_VN'), 'Tháng năm', 'May')
    assert(tz('2000-06-01', '%B', 'vi_VN'), 'Tháng sáu', 'June')
    assert(tz('2000-07-01', '%B', 'vi_VN'), 'Tháng bảy', 'July')
    assert(tz('2000-08-01', '%B', 'vi_VN'), 'Tháng tám', 'August')
    assert(tz('2000-09-01', '%B', 'vi_VN'), 'Tháng chín', 'September')
    assert(tz('2000-10-01', '%B', 'vi_VN'), 'Tháng mười', 'October')
    assert(tz('2000-11-01', '%B', 'vi_VN'), 'Tháng mười một', 'November')
    assert(tz('2000-12-01', '%B', 'vi_VN'), 'Tháng mười hai', 'December')
})
