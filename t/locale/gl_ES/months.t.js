require('proof')(24, function (assert) {
    var tz = require('timezone')(require('timezone/gl_ES'))

    // gl_ES abbreviated months
    assert(tz('2000-01-01', '%b', 'gl_ES'), 'Xan', 'Jan')
    assert(tz('2000-02-01', '%b', 'gl_ES'), 'Feb', 'Feb')
    assert(tz('2000-03-01', '%b', 'gl_ES'), 'Mar', 'Mar')
    assert(tz('2000-04-01', '%b', 'gl_ES'), 'Abr', 'Apr')
    assert(tz('2000-05-01', '%b', 'gl_ES'), 'Mai', 'May')
    assert(tz('2000-06-01', '%b', 'gl_ES'), 'Xuñ', 'Jun')
    assert(tz('2000-07-01', '%b', 'gl_ES'), 'Xul', 'Jul')
    assert(tz('2000-08-01', '%b', 'gl_ES'), 'Ago', 'Aug')
    assert(tz('2000-09-01', '%b', 'gl_ES'), 'Set', 'Sep')
    assert(tz('2000-10-01', '%b', 'gl_ES'), 'Out', 'Oct')
    assert(tz('2000-11-01', '%b', 'gl_ES'), 'Nov', 'Nov')
    assert(tz('2000-12-01', '%b', 'gl_ES'), 'Dec', 'Dec')

    // ' + name + ' months
    assert(tz('2000-01-01', '%B', 'gl_ES'), 'Xaneiro', 'January')
    assert(tz('2000-02-01', '%B', 'gl_ES'), 'Febreiro', 'February')
    assert(tz('2000-03-01', '%B', 'gl_ES'), 'Marzo', 'March')
    assert(tz('2000-04-01', '%B', 'gl_ES'), 'Abril', 'April')
    assert(tz('2000-05-01', '%B', 'gl_ES'), 'Maio', 'May')
    assert(tz('2000-06-01', '%B', 'gl_ES'), 'Xuño', 'June')
    assert(tz('2000-07-01', '%B', 'gl_ES'), 'Xullo', 'July')
    assert(tz('2000-08-01', '%B', 'gl_ES'), 'Agosto', 'August')
    assert(tz('2000-09-01', '%B', 'gl_ES'), 'Setembro', 'September')
    assert(tz('2000-10-01', '%B', 'gl_ES'), 'Outubro', 'October')
    assert(tz('2000-11-01', '%B', 'gl_ES'), 'Novembro', 'November')
    assert(tz('2000-12-01', '%B', 'gl_ES'), 'Decembro', 'December')
})
