require('proof')(24, function (assert) {
    var tz = require('timezone')(require('timezone/cs_CZ'))

    // cs_CZ abbreviated months
    assert(tz('2000-01-01', '%b', 'cs_CZ'), 'led', 'Jan')
    assert(tz('2000-02-01', '%b', 'cs_CZ'), 'úno', 'Feb')
    assert(tz('2000-03-01', '%b', 'cs_CZ'), 'bře', 'Mar')
    assert(tz('2000-04-01', '%b', 'cs_CZ'), 'dub', 'Apr')
    assert(tz('2000-05-01', '%b', 'cs_CZ'), 'kvě', 'May')
    assert(tz('2000-06-01', '%b', 'cs_CZ'), 'čen', 'Jun')
    assert(tz('2000-07-01', '%b', 'cs_CZ'), 'čec', 'Jul')
    assert(tz('2000-08-01', '%b', 'cs_CZ'), 'srp', 'Aug')
    assert(tz('2000-09-01', '%b', 'cs_CZ'), 'zář', 'Sep')
    assert(tz('2000-10-01', '%b', 'cs_CZ'), 'říj', 'Oct')
    assert(tz('2000-11-01', '%b', 'cs_CZ'), 'lis', 'Nov')
    assert(tz('2000-12-01', '%b', 'cs_CZ'), 'pro', 'Dec')

    // ' + name + ' months
    assert(tz('2000-01-01', '%B', 'cs_CZ'), 'leden', 'January')
    assert(tz('2000-02-01', '%B', 'cs_CZ'), 'únor', 'February')
    assert(tz('2000-03-01', '%B', 'cs_CZ'), 'březen', 'March')
    assert(tz('2000-04-01', '%B', 'cs_CZ'), 'duben', 'April')
    assert(tz('2000-05-01', '%B', 'cs_CZ'), 'květen', 'May')
    assert(tz('2000-06-01', '%B', 'cs_CZ'), 'červen', 'June')
    assert(tz('2000-07-01', '%B', 'cs_CZ'), 'červenec', 'July')
    assert(tz('2000-08-01', '%B', 'cs_CZ'), 'srpen', 'August')
    assert(tz('2000-09-01', '%B', 'cs_CZ'), 'září', 'September')
    assert(tz('2000-10-01', '%B', 'cs_CZ'), 'říjen', 'October')
    assert(tz('2000-11-01', '%B', 'cs_CZ'), 'listopad', 'November')
    assert(tz('2000-12-01', '%B', 'cs_CZ'), 'prosinec', 'December')
})
