require('proof')(24, function (assert) {
    var tz = require('timezone')(require('timezone/lt_LT'))

    // lt_LT abbreviated months
    assert(tz('2000-01-01', '%b', 'lt_LT'), 'Sau', 'Jan')
    assert(tz('2000-02-01', '%b', 'lt_LT'), 'Vas', 'Feb')
    assert(tz('2000-03-01', '%b', 'lt_LT'), 'Kov', 'Mar')
    assert(tz('2000-04-01', '%b', 'lt_LT'), 'Bal', 'Apr')
    assert(tz('2000-05-01', '%b', 'lt_LT'), 'Geg', 'May')
    assert(tz('2000-06-01', '%b', 'lt_LT'), 'Bir', 'Jun')
    assert(tz('2000-07-01', '%b', 'lt_LT'), 'Lie', 'Jul')
    assert(tz('2000-08-01', '%b', 'lt_LT'), 'Rgp', 'Aug')
    assert(tz('2000-09-01', '%b', 'lt_LT'), 'Rgs', 'Sep')
    assert(tz('2000-10-01', '%b', 'lt_LT'), 'Spa', 'Oct')
    assert(tz('2000-11-01', '%b', 'lt_LT'), 'Lap', 'Nov')
    assert(tz('2000-12-01', '%b', 'lt_LT'), 'Grd', 'Dec')

    // ' + name + ' months
    assert(tz('2000-01-01', '%B', 'lt_LT'), 'sausio', 'January')
    assert(tz('2000-02-01', '%B', 'lt_LT'), 'vasario', 'February')
    assert(tz('2000-03-01', '%B', 'lt_LT'), 'kovo', 'March')
    assert(tz('2000-04-01', '%B', 'lt_LT'), 'balandžio', 'April')
    assert(tz('2000-05-01', '%B', 'lt_LT'), 'gegužės', 'May')
    assert(tz('2000-06-01', '%B', 'lt_LT'), 'birželio', 'June')
    assert(tz('2000-07-01', '%B', 'lt_LT'), 'liepos', 'July')
    assert(tz('2000-08-01', '%B', 'lt_LT'), 'rugpjūčio', 'August')
    assert(tz('2000-09-01', '%B', 'lt_LT'), 'rugsėjo', 'September')
    assert(tz('2000-10-01', '%B', 'lt_LT'), 'spalio', 'October')
    assert(tz('2000-11-01', '%B', 'lt_LT'), 'lapkričio', 'November')
    assert(tz('2000-12-01', '%B', 'lt_LT'), 'gruodžio', 'December')
})
