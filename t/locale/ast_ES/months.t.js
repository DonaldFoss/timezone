require('proof')(24, function (assert) {
    var tz = require('timezone')(require('timezone/ast_ES'))

    // ast_ES abbreviated months
    assert(tz('2000-01-01', '%b', 'ast_ES'), 'xin', 'Jan')
    assert(tz('2000-02-01', '%b', 'ast_ES'), 'feb', 'Feb')
    assert(tz('2000-03-01', '%b', 'ast_ES'), 'mar', 'Mar')
    assert(tz('2000-04-01', '%b', 'ast_ES'), 'abr', 'Apr')
    assert(tz('2000-05-01', '%b', 'ast_ES'), 'may', 'May')
    assert(tz('2000-06-01', '%b', 'ast_ES'), 'xun', 'Jun')
    assert(tz('2000-07-01', '%b', 'ast_ES'), 'xnt', 'Jul')
    assert(tz('2000-08-01', '%b', 'ast_ES'), 'ago', 'Aug')
    assert(tz('2000-09-01', '%b', 'ast_ES'), 'set', 'Sep')
    assert(tz('2000-10-01', '%b', 'ast_ES'), 'och', 'Oct')
    assert(tz('2000-11-01', '%b', 'ast_ES'), 'pay', 'Nov')
    assert(tz('2000-12-01', '%b', 'ast_ES'), 'avi', 'Dec')

    // ' + name + ' months
    assert(tz('2000-01-01', '%B', 'ast_ES'), 'xineru', 'January')
    assert(tz('2000-02-01', '%B', 'ast_ES'), 'febreru', 'February')
    assert(tz('2000-03-01', '%B', 'ast_ES'), 'marzu', 'March')
    assert(tz('2000-04-01', '%B', 'ast_ES'), 'abril', 'April')
    assert(tz('2000-05-01', '%B', 'ast_ES'), 'mayu', 'May')
    assert(tz('2000-06-01', '%B', 'ast_ES'), 'xunu', 'June')
    assert(tz('2000-07-01', '%B', 'ast_ES'), 'xunetu', 'July')
    assert(tz('2000-08-01', '%B', 'ast_ES'), 'agostu', 'August')
    assert(tz('2000-09-01', '%B', 'ast_ES'), 'setiembre', 'September')
    assert(tz('2000-10-01', '%B', 'ast_ES'), 'ochobre', 'October')
    assert(tz('2000-11-01', '%B', 'ast_ES'), 'payares', 'November')
    assert(tz('2000-12-01', '%B', 'ast_ES'), 'avientu', 'December')
})
