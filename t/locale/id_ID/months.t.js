require('proof')(24, function (assert) {
    var tz = require('timezone')(require('timezone/id_ID'))

    // id_ID abbreviated months
    assert(tz('2000-01-01', '%b', 'id_ID'), 'Jan', 'Jan')
    assert(tz('2000-02-01', '%b', 'id_ID'), 'Peb', 'Feb')
    assert(tz('2000-03-01', '%b', 'id_ID'), 'Mar', 'Mar')
    assert(tz('2000-04-01', '%b', 'id_ID'), 'Apr', 'Apr')
    assert(tz('2000-05-01', '%b', 'id_ID'), 'Mei', 'May')
    assert(tz('2000-06-01', '%b', 'id_ID'), 'Jun', 'Jun')
    assert(tz('2000-07-01', '%b', 'id_ID'), 'Jul', 'Jul')
    assert(tz('2000-08-01', '%b', 'id_ID'), 'Agu', 'Aug')
    assert(tz('2000-09-01', '%b', 'id_ID'), 'Sep', 'Sep')
    assert(tz('2000-10-01', '%b', 'id_ID'), 'Okt', 'Oct')
    assert(tz('2000-11-01', '%b', 'id_ID'), 'Nov', 'Nov')
    assert(tz('2000-12-01', '%b', 'id_ID'), 'Des', 'Dec')

    // ' + name + ' months
    assert(tz('2000-01-01', '%B', 'id_ID'), 'Januari', 'January')
    assert(tz('2000-02-01', '%B', 'id_ID'), 'Pebruari', 'February')
    assert(tz('2000-03-01', '%B', 'id_ID'), 'Maret', 'March')
    assert(tz('2000-04-01', '%B', 'id_ID'), 'April', 'April')
    assert(tz('2000-05-01', '%B', 'id_ID'), 'Mei', 'May')
    assert(tz('2000-06-01', '%B', 'id_ID'), 'Juni', 'June')
    assert(tz('2000-07-01', '%B', 'id_ID'), 'Juli', 'July')
    assert(tz('2000-08-01', '%B', 'id_ID'), 'Agustus', 'August')
    assert(tz('2000-09-01', '%B', 'id_ID'), 'September', 'September')
    assert(tz('2000-10-01', '%B', 'id_ID'), 'Oktober', 'October')
    assert(tz('2000-11-01', '%B', 'id_ID'), 'November', 'November')
    assert(tz('2000-12-01', '%B', 'id_ID'), 'Desember', 'December')
})
