require('proof')(24, function (assert) {
    var tz = require('timezone')(require('timezone/nl_NL'))

    // nl_NL abbreviated months
    assert(tz('2000-01-01', '%b', 'nl_NL'), 'jan', 'Jan')
    assert(tz('2000-02-01', '%b', 'nl_NL'), 'feb', 'Feb')
    assert(tz('2000-03-01', '%b', 'nl_NL'), 'mrt', 'Mar')
    assert(tz('2000-04-01', '%b', 'nl_NL'), 'apr', 'Apr')
    assert(tz('2000-05-01', '%b', 'nl_NL'), 'mei', 'May')
    assert(tz('2000-06-01', '%b', 'nl_NL'), 'jun', 'Jun')
    assert(tz('2000-07-01', '%b', 'nl_NL'), 'jul', 'Jul')
    assert(tz('2000-08-01', '%b', 'nl_NL'), 'aug', 'Aug')
    assert(tz('2000-09-01', '%b', 'nl_NL'), 'sep', 'Sep')
    assert(tz('2000-10-01', '%b', 'nl_NL'), 'okt', 'Oct')
    assert(tz('2000-11-01', '%b', 'nl_NL'), 'nov', 'Nov')
    assert(tz('2000-12-01', '%b', 'nl_NL'), 'dec', 'Dec')

    // ' + name + ' months
    assert(tz('2000-01-01', '%B', 'nl_NL'), 'januari', 'January')
    assert(tz('2000-02-01', '%B', 'nl_NL'), 'februari', 'February')
    assert(tz('2000-03-01', '%B', 'nl_NL'), 'maart', 'March')
    assert(tz('2000-04-01', '%B', 'nl_NL'), 'april', 'April')
    assert(tz('2000-05-01', '%B', 'nl_NL'), 'mei', 'May')
    assert(tz('2000-06-01', '%B', 'nl_NL'), 'juni', 'June')
    assert(tz('2000-07-01', '%B', 'nl_NL'), 'juli', 'July')
    assert(tz('2000-08-01', '%B', 'nl_NL'), 'augustus', 'August')
    assert(tz('2000-09-01', '%B', 'nl_NL'), 'september', 'September')
    assert(tz('2000-10-01', '%B', 'nl_NL'), 'oktober', 'October')
    assert(tz('2000-11-01', '%B', 'nl_NL'), 'november', 'November')
    assert(tz('2000-12-01', '%B', 'nl_NL'), 'december', 'December')
})
