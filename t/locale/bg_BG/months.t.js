require('proof')(24, function (assert) {
    var tz = require('timezone')(require('timezone/bg_BG'))

    // bg_BG abbreviated months
    assert(tz('2000-01-01', '%b', 'bg_BG'), 'яну', 'Jan')
    assert(tz('2000-02-01', '%b', 'bg_BG'), 'фев', 'Feb')
    assert(tz('2000-03-01', '%b', 'bg_BG'), 'мар', 'Mar')
    assert(tz('2000-04-01', '%b', 'bg_BG'), 'апр', 'Apr')
    assert(tz('2000-05-01', '%b', 'bg_BG'), 'май', 'May')
    assert(tz('2000-06-01', '%b', 'bg_BG'), 'юни', 'Jun')
    assert(tz('2000-07-01', '%b', 'bg_BG'), 'юли', 'Jul')
    assert(tz('2000-08-01', '%b', 'bg_BG'), 'авг', 'Aug')
    assert(tz('2000-09-01', '%b', 'bg_BG'), 'сеп', 'Sep')
    assert(tz('2000-10-01', '%b', 'bg_BG'), 'окт', 'Oct')
    assert(tz('2000-11-01', '%b', 'bg_BG'), 'ное', 'Nov')
    assert(tz('2000-12-01', '%b', 'bg_BG'), 'дек', 'Dec')

    // ' + name + ' months
    assert(tz('2000-01-01', '%B', 'bg_BG'), 'януари', 'January')
    assert(tz('2000-02-01', '%B', 'bg_BG'), 'февруари', 'February')
    assert(tz('2000-03-01', '%B', 'bg_BG'), 'март', 'March')
    assert(tz('2000-04-01', '%B', 'bg_BG'), 'април', 'April')
    assert(tz('2000-05-01', '%B', 'bg_BG'), 'май', 'May')
    assert(tz('2000-06-01', '%B', 'bg_BG'), 'юни', 'June')
    assert(tz('2000-07-01', '%B', 'bg_BG'), 'юли', 'July')
    assert(tz('2000-08-01', '%B', 'bg_BG'), 'август', 'August')
    assert(tz('2000-09-01', '%B', 'bg_BG'), 'септември', 'September')
    assert(tz('2000-10-01', '%B', 'bg_BG'), 'октомври', 'October')
    assert(tz('2000-11-01', '%B', 'bg_BG'), 'ноември', 'November')
    assert(tz('2000-12-01', '%B', 'bg_BG'), 'декември', 'December')
})
