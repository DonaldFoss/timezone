require('proof')(24, function (assert) {
    var tz = require('timezone')(require('timezone/zh_CN'))

    // zh_CN abbreviated months
    assert(tz('2000-01-01', '%b', 'zh_CN'), ' 1月', 'Jan')
    assert(tz('2000-02-01', '%b', 'zh_CN'), ' 2月', 'Feb')
    assert(tz('2000-03-01', '%b', 'zh_CN'), ' 3月', 'Mar')
    assert(tz('2000-04-01', '%b', 'zh_CN'), ' 4月', 'Apr')
    assert(tz('2000-05-01', '%b', 'zh_CN'), ' 5月', 'May')
    assert(tz('2000-06-01', '%b', 'zh_CN'), ' 6月', 'Jun')
    assert(tz('2000-07-01', '%b', 'zh_CN'), ' 7月', 'Jul')
    assert(tz('2000-08-01', '%b', 'zh_CN'), ' 8月', 'Aug')
    assert(tz('2000-09-01', '%b', 'zh_CN'), ' 9月', 'Sep')
    assert(tz('2000-10-01', '%b', 'zh_CN'), '10月', 'Oct')
    assert(tz('2000-11-01', '%b', 'zh_CN'), '11月', 'Nov')
    assert(tz('2000-12-01', '%b', 'zh_CN'), '12月', 'Dec')

    // ' + name + ' months
    assert(tz('2000-01-01', '%B', 'zh_CN'), '一月', 'January')
    assert(tz('2000-02-01', '%B', 'zh_CN'), '二月', 'February')
    assert(tz('2000-03-01', '%B', 'zh_CN'), '三月', 'March')
    assert(tz('2000-04-01', '%B', 'zh_CN'), '四月', 'April')
    assert(tz('2000-05-01', '%B', 'zh_CN'), '五月', 'May')
    assert(tz('2000-06-01', '%B', 'zh_CN'), '六月', 'June')
    assert(tz('2000-07-01', '%B', 'zh_CN'), '七月', 'July')
    assert(tz('2000-08-01', '%B', 'zh_CN'), '八月', 'August')
    assert(tz('2000-09-01', '%B', 'zh_CN'), '九月', 'September')
    assert(tz('2000-10-01', '%B', 'zh_CN'), '十月', 'October')
    assert(tz('2000-11-01', '%B', 'zh_CN'), '十一月', 'November')
    assert(tz('2000-12-01', '%B', 'zh_CN'), '十二月', 'December')
})
