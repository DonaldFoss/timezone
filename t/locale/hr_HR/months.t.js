    assert(tz('2000-07-01', '%b', 'hr_HR'), 'Srp', 'Jul')
    assert(tz('2000-08-01', '%b', 'hr_HR'), 'Kol', 'Aug')
    assert(tz('2000-09-01', '%b', 'hr_HR'), 'Ruj', 'Sep')
    assert(tz('2000-10-01', '%b', 'hr_HR'), 'Lis', 'Oct')
    assert(tz('2000-11-01', '%b', 'hr_HR'), 'Stu', 'Nov')
