require('proof')(24, function (assert) {
    var tz = require('timezone')(require('timezone/sr_RS'))

    // sr_RS abbreviated months
    assert(tz('2000-01-01', '%b', 'sr_RS'), 'јан', 'Jan')
    assert(tz('2000-02-01', '%b', 'sr_RS'), 'феб', 'Feb')
    assert(tz('2000-03-01', '%b', 'sr_RS'), 'мар', 'Mar')
    assert(tz('2000-04-01', '%b', 'sr_RS'), 'апр', 'Apr')
    assert(tz('2000-05-01', '%b', 'sr_RS'), 'мај', 'May')
    assert(tz('2000-06-01', '%b', 'sr_RS'), 'јун', 'Jun')
    assert(tz('2000-07-01', '%b', 'sr_RS'), 'јул', 'Jul')
    assert(tz('2000-08-01', '%b', 'sr_RS'), 'авг', 'Aug')
    assert(tz('2000-09-01', '%b', 'sr_RS'), 'сеп', 'Sep')
    assert(tz('2000-10-01', '%b', 'sr_RS'), 'окт', 'Oct')
    assert(tz('2000-11-01', '%b', 'sr_RS'), 'нов', 'Nov')
    assert(tz('2000-12-01', '%b', 'sr_RS'), 'дец', 'Dec')

    // ' + name + ' months
    assert(tz('2000-01-01', '%B', 'sr_RS'), 'јануар', 'January')
    assert(tz('2000-02-01', '%B', 'sr_RS'), 'фебруар', 'February')
    assert(tz('2000-03-01', '%B', 'sr_RS'), 'март', 'March')
    assert(tz('2000-04-01', '%B', 'sr_RS'), 'април', 'April')
    assert(tz('2000-05-01', '%B', 'sr_RS'), 'мај', 'May')
    assert(tz('2000-06-01', '%B', 'sr_RS'), 'јун', 'June')
    assert(tz('2000-07-01', '%B', 'sr_RS'), 'јул', 'July')
    assert(tz('2000-08-01', '%B', 'sr_RS'), 'август', 'August')
    assert(tz('2000-09-01', '%B', 'sr_RS'), 'септембар', 'September')
    assert(tz('2000-10-01', '%B', 'sr_RS'), 'октобар', 'October')
    assert(tz('2000-11-01', '%B', 'sr_RS'), 'новембар', 'November')
    assert(tz('2000-12-01', '%B', 'sr_RS'), 'децембар', 'December')
})
