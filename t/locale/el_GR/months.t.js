require('proof')(24, function (assert) {
    var tz = require('timezone')(require('timezone/el_GR'))

    // el_GR abbreviated months
    assert(tz('2000-01-01', '%b', 'el_GR'), 'Ιαν', 'Jan')
    assert(tz('2000-02-01', '%b', 'el_GR'), 'Φεβ', 'Feb')
    assert(tz('2000-03-01', '%b', 'el_GR'), 'Μάρ', 'Mar')
    assert(tz('2000-04-01', '%b', 'el_GR'), 'Απρ', 'Apr')
    assert(tz('2000-05-01', '%b', 'el_GR'), 'Μάι', 'May')
    assert(tz('2000-06-01', '%b', 'el_GR'), 'Ιούν', 'Jun')
    assert(tz('2000-07-01', '%b', 'el_GR'), 'Ιούλ', 'Jul')
    assert(tz('2000-08-01', '%b', 'el_GR'), 'Αύγ', 'Aug')
    assert(tz('2000-09-01', '%b', 'el_GR'), 'Σεπ', 'Sep')
    assert(tz('2000-10-01', '%b', 'el_GR'), 'Οκτ', 'Oct')
    assert(tz('2000-11-01', '%b', 'el_GR'), 'Νοέ', 'Nov')
    assert(tz('2000-12-01', '%b', 'el_GR'), 'Δεκ', 'Dec')

    // ' + name + ' months
    assert(tz('2000-01-01', '%B', 'el_GR'), 'Ιανουάριος', 'January')
    assert(tz('2000-02-01', '%B', 'el_GR'), 'Φεβρουάριος', 'February')
    assert(tz('2000-03-01', '%B', 'el_GR'), 'Μάρτιος', 'March')
    assert(tz('2000-04-01', '%B', 'el_GR'), 'Απρίλιος', 'April')
    assert(tz('2000-05-01', '%B', 'el_GR'), 'Μάιος', 'May')
    assert(tz('2000-06-01', '%B', 'el_GR'), 'Ιούνιος', 'June')
    assert(tz('2000-07-01', '%B', 'el_GR'), 'Ιούλιος', 'July')
    assert(tz('2000-08-01', '%B', 'el_GR'), 'Αύγουστος', 'August')
    assert(tz('2000-09-01', '%B', 'el_GR'), 'Σεπτέμβριος', 'September')
    assert(tz('2000-10-01', '%B', 'el_GR'), 'Οκτώβριος', 'October')
    assert(tz('2000-11-01', '%B', 'el_GR'), 'Νοέμβριος', 'November')
    assert(tz('2000-12-01', '%B', 'el_GR'), 'Δεκέμβριος', 'December')
})
