require('proof')(24, function (assert) {
    var tz = require('timezone')(require('timezone/sl_SI'))

    // sl_SI abbreviated months
    assert(tz('2000-01-01', '%b', 'sl_SI'), 'jan', 'Jan')
    assert(tz('2000-02-01', '%b', 'sl_SI'), 'feb', 'Feb')
    assert(tz('2000-03-01', '%b', 'sl_SI'), 'mar', 'Mar')
    assert(tz('2000-04-01', '%b', 'sl_SI'), 'apr', 'Apr')
    assert(tz('2000-05-01', '%b', 'sl_SI'), 'maj', 'May')
    assert(tz('2000-06-01', '%b', 'sl_SI'), 'jun', 'Jun')
    assert(tz('2000-07-01', '%b', 'sl_SI'), 'jul', 'Jul')
    assert(tz('2000-08-01', '%b', 'sl_SI'), 'avg', 'Aug')
    assert(tz('2000-09-01', '%b', 'sl_SI'), 'sep', 'Sep')
    assert(tz('2000-10-01', '%b', 'sl_SI'), 'okt', 'Oct')
    assert(tz('2000-11-01', '%b', 'sl_SI'), 'nov', 'Nov')
    assert(tz('2000-12-01', '%b', 'sl_SI'), 'dec', 'Dec')

    // ' + name + ' months
    assert(tz('2000-01-01', '%B', 'sl_SI'), 'januar', 'January')
    assert(tz('2000-02-01', '%B', 'sl_SI'), 'februar', 'February')
    assert(tz('2000-03-01', '%B', 'sl_SI'), 'marec', 'March')
    assert(tz('2000-04-01', '%B', 'sl_SI'), 'april', 'April')
    assert(tz('2000-05-01', '%B', 'sl_SI'), 'maj', 'May')
    assert(tz('2000-06-01', '%B', 'sl_SI'), 'junij', 'June')
    assert(tz('2000-07-01', '%B', 'sl_SI'), 'julij', 'July')
    assert(tz('2000-08-01', '%B', 'sl_SI'), 'avgust', 'August')
    assert(tz('2000-09-01', '%B', 'sl_SI'), 'september', 'September')
    assert(tz('2000-10-01', '%B', 'sl_SI'), 'oktober', 'October')
    assert(tz('2000-11-01', '%B', 'sl_SI'), 'november', 'November')
    assert(tz('2000-12-01', '%B', 'sl_SI'), 'december', 'December')
})
