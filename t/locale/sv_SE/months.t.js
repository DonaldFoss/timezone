require('proof')(24, function (assert) {
    var tz = require('timezone')(require('timezone/sv_SE'))

    // sv_SE abbreviated months
    assert(tz('2000-01-01', '%b', 'sv_SE'), 'jan', 'Jan')
    assert(tz('2000-02-01', '%b', 'sv_SE'), 'feb', 'Feb')
    assert(tz('2000-03-01', '%b', 'sv_SE'), 'mar', 'Mar')
    assert(tz('2000-04-01', '%b', 'sv_SE'), 'apr', 'Apr')
    assert(tz('2000-05-01', '%b', 'sv_SE'), 'maj', 'May')
    assert(tz('2000-06-01', '%b', 'sv_SE'), 'jun', 'Jun')
    assert(tz('2000-07-01', '%b', 'sv_SE'), 'jul', 'Jul')
    assert(tz('2000-08-01', '%b', 'sv_SE'), 'aug', 'Aug')
    assert(tz('2000-09-01', '%b', 'sv_SE'), 'sep', 'Sep')
    assert(tz('2000-10-01', '%b', 'sv_SE'), 'okt', 'Oct')
    assert(tz('2000-11-01', '%b', 'sv_SE'), 'nov', 'Nov')
    assert(tz('2000-12-01', '%b', 'sv_SE'), 'dec', 'Dec')

    // ' + name + ' months
    assert(tz('2000-01-01', '%B', 'sv_SE'), 'januari', 'January')
    assert(tz('2000-02-01', '%B', 'sv_SE'), 'februari', 'February')
    assert(tz('2000-03-01', '%B', 'sv_SE'), 'mars', 'March')
    assert(tz('2000-04-01', '%B', 'sv_SE'), 'april', 'April')
    assert(tz('2000-05-01', '%B', 'sv_SE'), 'maj', 'May')
    assert(tz('2000-06-01', '%B', 'sv_SE'), 'juni', 'June')
    assert(tz('2000-07-01', '%B', 'sv_SE'), 'juli', 'July')
    assert(tz('2000-08-01', '%B', 'sv_SE'), 'augusti', 'August')
    assert(tz('2000-09-01', '%B', 'sv_SE'), 'september', 'September')
    assert(tz('2000-10-01', '%B', 'sv_SE'), 'oktober', 'October')
    assert(tz('2000-11-01', '%B', 'sv_SE'), 'november', 'November')
    assert(tz('2000-12-01', '%B', 'sv_SE'), 'december', 'December')
})
