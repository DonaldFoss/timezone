require('proof')(24, function (assert) {
    var tz = require('timezone')(require('timezone/it_CH'))

    // it_CH abbreviated months
    assert(tz('2000-01-01', '%b', 'it_CH'), 'gen', 'Jan')
    assert(tz('2000-02-01', '%b', 'it_CH'), 'feb', 'Feb')
    assert(tz('2000-03-01', '%b', 'it_CH'), 'mar', 'Mar')
    assert(tz('2000-04-01', '%b', 'it_CH'), 'apr', 'Apr')
    assert(tz('2000-05-01', '%b', 'it_CH'), 'mag', 'May')
    assert(tz('2000-06-01', '%b', 'it_CH'), 'giu', 'Jun')
    assert(tz('2000-07-01', '%b', 'it_CH'), 'lug', 'Jul')
    assert(tz('2000-08-01', '%b', 'it_CH'), 'ago', 'Aug')
    assert(tz('2000-09-01', '%b', 'it_CH'), 'set', 'Sep')
    assert(tz('2000-10-01', '%b', 'it_CH'), 'ott', 'Oct')
    assert(tz('2000-11-01', '%b', 'it_CH'), 'nov', 'Nov')
    assert(tz('2000-12-01', '%b', 'it_CH'), 'dic', 'Dec')

    // ' + name + ' months
    assert(tz('2000-01-01', '%B', 'it_CH'), 'gennaio', 'January')
    assert(tz('2000-02-01', '%B', 'it_CH'), 'febbraio', 'February')
    assert(tz('2000-03-01', '%B', 'it_CH'), 'marzo', 'March')
    assert(tz('2000-04-01', '%B', 'it_CH'), 'aprile', 'April')
    assert(tz('2000-05-01', '%B', 'it_CH'), 'maggio', 'May')
    assert(tz('2000-06-01', '%B', 'it_CH'), 'giugno', 'June')
    assert(tz('2000-07-01', '%B', 'it_CH'), 'luglio', 'July')
    assert(tz('2000-08-01', '%B', 'it_CH'), 'agosto', 'August')
    assert(tz('2000-09-01', '%B', 'it_CH'), 'settembre', 'September')
    assert(tz('2000-10-01', '%B', 'it_CH'), 'ottobre', 'October')
    assert(tz('2000-11-01', '%B', 'it_CH'), 'novembre', 'November')
    assert(tz('2000-12-01', '%B', 'it_CH'), 'dicembre', 'December')
})
