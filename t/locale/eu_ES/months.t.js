require('proof')(24, function (assert) {
    var tz = require('timezone')(require('timezone/eu_ES'))

    // eu_ES abbreviated months
    assert(tz('2000-01-01', '%b', 'eu_ES'), 'urt', 'Jan')
    assert(tz('2000-02-01', '%b', 'eu_ES'), 'ots', 'Feb')
    assert(tz('2000-03-01', '%b', 'eu_ES'), 'mar', 'Mar')
    assert(tz('2000-04-01', '%b', 'eu_ES'), 'api', 'Apr')
    assert(tz('2000-05-01', '%b', 'eu_ES'), 'mai', 'May')
    assert(tz('2000-06-01', '%b', 'eu_ES'), 'eka', 'Jun')
    assert(tz('2000-07-01', '%b', 'eu_ES'), 'uzt', 'Jul')
    assert(tz('2000-08-01', '%b', 'eu_ES'), 'abu', 'Aug')
    assert(tz('2000-09-01', '%b', 'eu_ES'), 'ira', 'Sep')
    assert(tz('2000-10-01', '%b', 'eu_ES'), 'urr', 'Oct')
    assert(tz('2000-11-01', '%b', 'eu_ES'), 'aza', 'Nov')
    assert(tz('2000-12-01', '%b', 'eu_ES'), 'abe', 'Dec')

    // ' + name + ' months
    assert(tz('2000-01-01', '%B', 'eu_ES'), 'urtarrila', 'January')
    assert(tz('2000-02-01', '%B', 'eu_ES'), 'otsaila', 'February')
    assert(tz('2000-03-01', '%B', 'eu_ES'), 'martxoa', 'March')
    assert(tz('2000-04-01', '%B', 'eu_ES'), 'apirila', 'April')
    assert(tz('2000-05-01', '%B', 'eu_ES'), 'maiatza', 'May')
    assert(tz('2000-06-01', '%B', 'eu_ES'), 'ekaina', 'June')
    assert(tz('2000-07-01', '%B', 'eu_ES'), 'uztaila', 'July')
    assert(tz('2000-08-01', '%B', 'eu_ES'), 'abuztua', 'August')
    assert(tz('2000-09-01', '%B', 'eu_ES'), 'iraila', 'September')
    assert(tz('2000-10-01', '%B', 'eu_ES'), 'urria', 'October')
    assert(tz('2000-11-01', '%B', 'eu_ES'), 'azaroa', 'November')
    assert(tz('2000-12-01', '%B', 'eu_ES'), 'abendua', 'December')
})
