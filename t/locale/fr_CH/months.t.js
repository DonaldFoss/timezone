require('proof')(24, function (assert) {
    var tz = require('timezone')(require('timezone/fr_CH'))

    // fr_CH abbreviated months
    assert(tz('2000-01-01', '%b', 'fr_CH'), 'jan', 'Jan')
    assert(tz('2000-02-01', '%b', 'fr_CH'), 'fév', 'Feb')
    assert(tz('2000-03-01', '%b', 'fr_CH'), 'mar', 'Mar')
    assert(tz('2000-04-01', '%b', 'fr_CH'), 'avr', 'Apr')
    assert(tz('2000-05-01', '%b', 'fr_CH'), 'mai', 'May')
    assert(tz('2000-06-01', '%b', 'fr_CH'), 'jun', 'Jun')
    assert(tz('2000-07-01', '%b', 'fr_CH'), 'jui', 'Jul')
    assert(tz('2000-08-01', '%b', 'fr_CH'), 'aoû', 'Aug')
    assert(tz('2000-09-01', '%b', 'fr_CH'), 'sep', 'Sep')
    assert(tz('2000-10-01', '%b', 'fr_CH'), 'oct', 'Oct')
    assert(tz('2000-11-01', '%b', 'fr_CH'), 'nov', 'Nov')
    assert(tz('2000-12-01', '%b', 'fr_CH'), 'déc', 'Dec')

    // ' + name + ' months
    assert(tz('2000-01-01', '%B', 'fr_CH'), 'janvier', 'January')
    assert(tz('2000-02-01', '%B', 'fr_CH'), 'février', 'February')
    assert(tz('2000-03-01', '%B', 'fr_CH'), 'mars', 'March')
    assert(tz('2000-04-01', '%B', 'fr_CH'), 'avril', 'April')
    assert(tz('2000-05-01', '%B', 'fr_CH'), 'mai', 'May')
    assert(tz('2000-06-01', '%B', 'fr_CH'), 'juin', 'June')
    assert(tz('2000-07-01', '%B', 'fr_CH'), 'juillet', 'July')
    assert(tz('2000-08-01', '%B', 'fr_CH'), 'août', 'August')
    assert(tz('2000-09-01', '%B', 'fr_CH'), 'septembre', 'September')
    assert(tz('2000-10-01', '%B', 'fr_CH'), 'octobre', 'October')
    assert(tz('2000-11-01', '%B', 'fr_CH'), 'novembre', 'November')
    assert(tz('2000-12-01', '%B', 'fr_CH'), 'décembre', 'December')
})
