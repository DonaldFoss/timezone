    assert(tz('2000-04-01', '%b', 'pt_PT'), 'Abr', 'Apr')
    assert(tz('2000-05-01', '%b', 'pt_PT'), 'Mai', 'May')
    assert(tz('2000-06-01', '%b', 'pt_PT'), 'Jun', 'Jun')
    assert(tz('2000-07-01', '%b', 'pt_PT'), 'Jul', 'Jul')
    assert(tz('2000-08-01', '%b', 'pt_PT'), 'Ago', 'Aug')
    assert(tz('2000-09-01', '%b', 'pt_PT'), 'Set', 'Sep')
    assert(tz('2000-10-01', '%b', 'pt_PT'), 'Out', 'Oct')
    assert(tz('2000-11-01', '%b', 'pt_PT'), 'Nov', 'Nov')
    assert(tz('2000-12-01', '%b', 'pt_PT'), 'Dez', 'Dec')

